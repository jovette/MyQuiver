namespace MyQuiver.Common
{
    /// <summary>
    /// Length of International Limb Fitting
    /// </summary>
    public enum LimbLength
    {
        /// <summary>
        /// Short ILF limbs make for a 168 cm bow (173 cm on 69 cm riser)
        /// </summary>
        Short,

        /// <summary>
        /// Medium ILF limbs make for a 173 cm bow (178 cm on 69 cm riser)
        /// </summary>
        Medium,

        /// <summary>
        /// Long ILF limbs make for a 178 cm bow (183 cm on 69 cm riser)
        /// </summary>
        Long
    }
}
